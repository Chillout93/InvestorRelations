<h1>Company Profile</h1>
<h2 class="headertop">Company Information</h2>
<p>Mulberry is an English design company renowned world-wide for its craftsmanship and quality. 
The brand's backbone is an extensive line of luxury fashion bags for men and women that combine stylish,
stand-out design with the finest leathers and detailing. In addition to leather goods, 
Mulberry's product range includes womenswear, accessories and footwear.</p>

<p>The Group's principal activities are the design and manufacture or sourcing of products and their subsequent
sale through wholesale channels or the Group’s own shops in home and export markets.</p>

<p>The Group trades internationally either directly through its own retail stores or through direct wholesale 
customers and partners. Its operations are largely based in the United Kingdom.</p>

<p>We have two large head offices in the UK, in Somerset and London. We also have two UK factories based in Somerset, 
known as The Rookery and The Willows.</p>

<p>Mulberry Group plc is an AIM listed company (registration number 1180514). The company is registered in England and Wales and the registered address is:</p>

<p>Mulberry Group plc The Rookery, Chilcompton, Bath, Somerset BA3 4EH England</p>

<p>Mulberry Group plc is subject to the UK City Code on Takeovers and Mergers.</p>

