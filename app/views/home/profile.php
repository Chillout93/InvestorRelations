<h4>California Transparency in Supply Chains Act</h4>
<p>Under the California Transparency in Supply Chains Act of 2010, companies of a certain size doing business in the State of California are 
obliged to disclose their efforts to eradicate slavery and human trafficking from their direct product supply chains for the goods that they offer for sale.</p>

<p>All of Mulberry’s supplier relationships are governed by Mulberry’s Global Sourcing Principles which includes the absolute prohibition of forced or bonded labour. 
In this disclosure we explain in more detail how we apply this policy in practice through good governance practices.</p>

