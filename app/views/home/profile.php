<h3>People & Fair Partners</h3>
<h4 class="headertop">Apprentices</h4>
<p>In 2006 we took on our first apprentices through a new Government approved scheme supported by Skillset UK and Bridgwater College. The year-long training course, 
comprising a leather skills NVQ and technical certificate, sets young apprentices on the path to becoming accomplished craftsmen and craftswomen. 
In the nine years since we started, more than 80 apprentices have completed the course. Some of these apprentices have gone on to apply their skills 
and knowledge to different roles in the company for example product development, planning and market support.</p>

<p>As a result of the success of the programme, we have a waiting list of young people aspiring to join our production line team.</p>

<p>The programme has also received national recognition. In February 2010, we were proud to receive the National Skills Award, given to acknowledge 
excellence and world class performance in manufacturing. In 2014 we received the Business in the Community Big Tick Award in the Inspiring Young Talent category.</p>

<h4>Manufacturing</h4>
<p>We are proud to produce over 50% of our leather goods in our British factories, The Rookery and The Willows. Mulberry is the largest manufacturer of luxury goods in the
UK and employs over 550 people between the two factories.</p>

<p>We also work with a select group of suppliers elsewhere in the UK and overseas. We expect these suppliers to create a suitable environment for 
their workers, and ask them to adhere to a set of standards called the Mulberry Global Sourcing Principles. We are committed to enforcing these 
standards though regular inspections of supplier facilities by our own employees and by third-party auditors.</p>

