<h4>Environment</h4>
<h5 class="headertop">Sustainable buildings</h5>
<p>Investing in the latest technologies, along with awareness raising activities among employees, are just two methods we are using to help 
reduce our energy consumption. Both of our UK factories have rainwater harvesting for grey water use, and we have solar thermal panels to 
heat the water at our Chilcompton factory. At our Bridgwater factory, the Willows, the installation of 290m² solar panels will generate power 
for approximately 10% of the site’s energy needs.</p>

<p>Mulberry worked with specialist energy auditors to carry out for site energy audits in 2014/15 across our office, factory, 
warehouse and retail sites to comply with the government's Energy Saving Opportunities Scheme. The report found that 'a significant 
amount of work has already been completed to improve efficiency' but that across the estate 'lighting, heating and cooling are significant 
energy users and should be examined closely for energy saving opportunities.' A number of energy saving measures were identified, with 
opportunities for Mulberry to save over 10% of our total energy spend.</p>

<h5>Animal Welfare & Biodiversity</h5>
<p>We are committed to ethical practices in the catching, maintaining, breeding, raising, transportation, handling, and slaughter of animals in our leather, 
fur and exotic skins supply chains.</p>

<p>We are committed to sustaining well-functioning and bio-diverse ecosystems in the regions where we source. This includes maintaining species populations of wild
animals at sustainable levels as well as to only source material of animal origin from legal sources.</p>

<p>We collect traceability information on all our leather articles, so we know if we can trace it to the farm, to the abattoir or to the country of 
origin. Traceability is the tool by which we can understand the wider environmental and social impacts in our leather supply chain, including animal 
welfare, land use impacts and how the leather was made.</p>

<p>Since 2014 Mulberry has made annual disclosures to the CDP Forests Program on our forest-risk commodity purchasing practices.</p>

<p>We carry out research on welfare issues on the various materials we source, and the regions where we source from, also collaborating on 
research with the Sustainable Luxury Working Group, a group of companies that produce luxury goods and that share a commitment to advancing good 
social, environmental and animal welfare practices in their operations.</p>

<h4>Recycling</h4>
<p>We recycle as much material as possible from our UK sites. We have set up a number of partnerships with scrap stores local to our Somerset factories, 
passing on a significant quantity of our leather off-cuts to community arts and crafts groups. We are actively seeking other upcycling and recycling 
opportunities for our leather offcuts.</p>

