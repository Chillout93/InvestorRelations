<h2 id="board_information">Board Information</h2>

<h3 class="headertop">Board responsibility</h3>
<p>The Board is responsible for formulating, reviewing and approving the Group’s strategy; in addition it has overall responsibility for 
the budgets and corporate actions. The Board recognises the importance of high standards of corporate governance and supports the principles 
contained in the Revised Code, applying them where they consider them appropriate to Mulberry Group plc.</p>

<h3>Nominations And Remuneration Committee</h3>
<p>The Nominations and Remuneration Committee is chaired by a Non-Executive Director, Chris Roberts. The Committee is responsible for determining
the remuneration and terms and conditions of employment of Executive Directors and senior employees of the Group.</p>

<h3>Audit Committee</h3>
<p>The Audit Committee is chaired by a Non-Executive Director, Steven Grapstein. The Committee may examine any matters relating to the 
financial affairs of the Group. This includes review of the annual financial statements prior to their approval by the Board, together 
with accounting policies and compliance with accounting standards, and of internal control procedures and monthly financial reporting, 
and other related functions as the Committee may require. The Non-Executive Directors have access to the Group's auditors and legal advisers 
at any time without Executive Directors being present.</p>

