<h3>Long Term Incentive Plan (LTIP)</h3>
<p>On 19th December 2012 Mulberry established a LTIP to operate in conjunction with the Employee Benefit Trust. The LTIP is designed to align 
management and shareholders’ interests through rewarding participants for growth in Mulberry’s turnover and earnings before interest and tax (EBIT) above 
specified thresholds over the vesting period. 50% of the shares granted vest in accordance with the Revenue growth target and 50% in accordance with the EBIT target.</p>

<p>Options vest on a straight line pro rata basis between the minimum and maximum thresholds for both Revenue and EBIT. In the event that targets are not met, 
the options lapse.</p>

<p>The first grant of nil cost options was made on 20th December 2012. These options lapsed on their 30th June 2015 vesting date 
as the performance targets were not met.</p>

<p>A second grant of nil cost options was made on 7th August 2013. These options lapsed on their 30th June 2016 vesting date as the performance targets were not met.</p>

